/**
 * Generate src/engine/synthetic/measure-bindings.ts from the YAML measure definitions
 * (measures/*.yaml, ADR-006). The synthetic FHIR bundle builder stamps these codes/value
 * sets per employee. Re-run on measure re-seed:
 *   node scripts/gen-measure-bindings.mjs
 */
import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const here = path.dirname(fileURLToPath(import.meta.url));
// Source corpus lives in backend-ts/measures/ (relocated from the retired backend/ in #109 PR4).
const dir = path.resolve(here, "../measures");
const outFile = path.resolve(here, "../src/engine/synthetic/measure-bindings.ts");

const line = (s, key) => s.match(new RegExp(`^\\s*${key}:\\s*(.+)$`, "m"))?.[1].trim();
const inField = (block, k) => block.match(new RegExp(`${k}:\\s*"?([^,"}]+?)"?\\s*[,}]`))?.[1].trim() ?? null;

const out = [];
for (const f of readdirSync(dir).filter((x) => x.endsWith(".yaml")).sort()) {
  const s = readFileSync(path.join(dir, f), "utf8");
  const id = line(s, "id");
  const en = line(s, "enrollment") ?? "";
  const wv = line(s, "waiver") ?? "";
  const ev = line(s, "event") ?? "";
  const rf = line(s, "refusal");
  const cc = line(s, "complianceClass") ?? "RECURRING";
  const sr = line(s, "series");
  out.push({
    id,
    rateKey: line(s, "rateKey") ?? id,
    complianceClass: cc.trim().toUpperCase() === "PERMANENT" ? "PERMANENT" : "RECURRING",
    complianceWindowDays: Number(line(s, "complianceWindowDays") ?? 365),
    enrollment: { code: inField(en, "code"), valueSet: inField(en, "valueSet") },
    waiver: { code: inField(wv, "code"), valueSet: inField(wv, "valueSet") },
    event: { code: inField(ev, "code"), valueSet: inField(ev, "valueSet"), type: inField(ev, "type") },
    refusal: rf ? { code: inField(rf, "code"), valueSet: inField(rf, "valueSet") } : undefined,
    series: sr ? { requiredDoses: Number(inField(sr, "requiredDoses") ?? 2) } : undefined,
    // E11.2a: a windowed measure's grace period (from the `rule:` block) — emitted only when nonzero so
    // existing measures' lines are unchanged. deriveWhyFlagged uses it for grace-aware days_overdue.
    gracePeriodDays: Number(line(s, "gracePeriodDays") ?? 0),
  });
}

const body = out
  .map((b) => {
    const refusal = b.refusal ? `, refusal: ${JSON.stringify(b.refusal)}` : "";
    const series = b.series ? `, series: ${JSON.stringify(b.series)}` : "";
    const grace = b.gracePeriodDays ? `, gracePeriodDays: ${b.gracePeriodDays}` : "";
    return (
      `  ${JSON.stringify(b.id)}: { rateKey: ${JSON.stringify(b.rateKey)}, complianceClass: ${JSON.stringify(b.complianceClass)}, complianceWindowDays: ${b.complianceWindowDays}, ` +
      `enrollment: ${JSON.stringify(b.enrollment)}, waiver: ${JSON.stringify(b.waiver)}, event: ${JSON.stringify(b.event)}${refusal}${series}${grace} },`
    );
  })
  .join("\n");

const ts = `// AUTO-GENERATED by scripts/gen-measure-bindings.mjs from the YAML measure defs — do not edit.
/**
 * Synthetic measure bindings (#107 run pipeline) — the codes/value sets + compliance
 * window the synthetic FHIR bundle builder stamps per employee. \`event.type\` drives the
 * resource kind: procedure | immunization | observation. Source of truth: the YAML
 * measure definitions (ADR-006); regenerate on re-seed.
 */
export type EventType = "procedure" | "immunization" | "observation";

export interface CodeBinding {
  code: string;
  valueSet: string;
}

export interface SeriesBinding {
  requiredDoses: number;
}

export interface MeasureBinding {
  rateKey: string;
  complianceClass: "PERMANENT" | "RECURRING";
  complianceWindowDays: number;
  enrollment: CodeBinding;
  waiver: CodeBinding;
  event: CodeBinding & { type: EventType };
  refusal?: CodeBinding;
  series?: SeriesBinding;
  gracePeriodDays?: number;
}

export const MEASURE_BINDINGS: Record<string, MeasureBinding> = {
${body}
};
`;
writeFileSync(outFile, ts);
console.log(`wrote measure-bindings.ts for ${out.length} measures`);
console.log(out.map((o) => `${o.id}:${o.event.type}:w${o.complianceWindowDays}`).join("  "));
