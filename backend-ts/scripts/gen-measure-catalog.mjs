/**
 * Generates src/measure/measure-catalog.ts — the full 60-measure TWH catalog the
 * `/measures` page reads — from the Java seed (the single source of truth):
 *   - 49 CMS eCQM entries parsed from MeasureService.CMS_ECQM_CATALOG (47 Draft +
 *     CMS125v14/CMS122v14 promoted to Active),
 *   - 8 Active runnable OSHA/HEDIS measures (ids aligned with the engine registry),
 *   - 3 OSHA catalog-only measures from V017__seed_additional_measures.sql.
 *
 * Run from backend-ts/:  node scripts/gen-measure-catalog.mjs
 * Mirrors scripts/gen-measure-bindings.mjs / the employee-catalog generator pattern.
 */
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";

const here = fileURLToPath(new URL(".", import.meta.url));
const javaSvc = `${here}../../backend/src/main/java/com/workwell/measure/MeasureService.java`;
const outFile = `${here}../src/measure/measure-catalog.ts`;

// --- 1. CMS eCQM catalog, parsed from MeasureService.CMS_ECQM_CATALOG ----------
const java = readFileSync(javaSvc, "utf8");
const re = /new CmsEcqmRecord\("(.+?)",\s*"(.+?)",\s*"(.+?)",\s*"\{(.+?)\}"\)/g;
// CMS ids that are promoted to Active (full CQL) by their dedicated seed methods.
const ACTIVE_CMS = { CMS125v14: "cms125", CMS122v14: "cms122" };
const cms = [];
for (const m of java.matchAll(re)) {
  const [, name, cmsId, , tagsRaw] = m;
  const active = Object.prototype.hasOwnProperty.call(ACTIVE_CMS, cmsId);
  cms.push({
    id: active ? ACTIVE_CMS[cmsId] : cmsId.toLowerCase(),
    name,
    policyRef: cmsId,
    version: "v1.0",
    status: active ? "Active" : "Draft",
    owner: "WorkWell Studio",
    tags: tagsRaw.split(",").map((t) => t.trim()),
  });
}
if (cms.length !== 49) throw new Error(`expected 49 CMS catalog entries, parsed ${cms.length}`);

// --- 2. Active runnable OSHA + HEDIS measures (ids aligned with measure-registry) -
const osha = (id, name, policyRef, tags) => ({ id, name, policyRef, version: "v1.0", status: "Active", owner: "system", tags });
const runnable = [
  osha("audiogram", "Annual Audiogram Completed", "OSHA 29 CFR 1910.95", ["surveillance", "hearing", "osha"]),
  osha("hazwoper", "HAZWOPER Surveillance", "OSHA 29 CFR 1910.120", ["surveillance", "hazmat", "osha"]),
  osha("tb_surveillance", "TB Surveillance", "CDC TB Screening Guidance", ["surveillance", "infection-control", "cdc"]),
  osha("flu_vaccine", "Flu Vaccine", "CDC Seasonal Influenza Guidance", ["vaccine", "seasonal", "immunization"]),
  osha("hypertension", "Hypertension BP Screening", "HEDIS BPC / JPMC Wellness Rewards", ["wellness", "hypertension", "cardiovascular"]),
  osha("diabetes_hba1c", "Diabetes HbA1c Monitoring", "HEDIS HBD / JPMC Wellness Rewards", ["wellness", "diabetes", "hba1c"]),
  osha("obesity_bmi", "BMI Screening & Counseling", "HEDIS WCC / Cigna Healthcare Wellness", ["wellness", "bmi", "obesity"]),
  osha("cholesterol_ldl", "Cholesterol LDL Screening", "HEDIS CBP / JPMC Wellness Rewards", ["wellness", "cholesterol", "cardiovascular"]),
];

// --- 3. OSHA catalog-only measures (V017__seed_additional_measures.sql) ----------
const catalogOnly = [
  { id: "respirator_fit_test", name: "Respirator Fit Test", policyRef: "OSHA 29 CFR 1910.134", version: "v0.9", status: "Draft", owner: "J. Chen", tags: ["surveillance", "respiratory", "osha"] },
  { id: "hepatitis_b_vaccination_series", name: "Hepatitis B Vaccination Series", policyRef: "OSHA 29 CFR 1910.1030", version: "v2.0", status: "Approved", owner: "K. Williams", tags: ["vaccine", "bbp", "osha"] },
  { id: "lead_medical_surveillance", name: "Lead Medical Surveillance", policyRef: "OSHA 29 CFR 1910.1025", version: "v1.1", status: "Deprecated", owner: "M. Patel", tags: ["surveillance", "lead", "osha"] },
];

const all = [...runnable, ...catalogOnly, ...cms];
if (all.length !== 60) throw new Error(`expected 60 catalog measures, built ${all.length}`);
const ids = new Set(all.map((m) => m.id));
if (ids.size !== 60) throw new Error("duplicate measure id in the generated catalog");

const body = all
  .map(
    (m) =>
      `  { id: ${JSON.stringify(m.id)}, name: ${JSON.stringify(m.name)}, policyRef: ${JSON.stringify(m.policyRef)}, ` +
      `version: ${JSON.stringify(m.version)}, status: ${JSON.stringify(m.status)}, owner: ${JSON.stringify(m.owner)}, ` +
      `tags: ${JSON.stringify(m.tags)} },`,
  )
  .join("\n");

const out = `/**
 * GENERATED by scripts/gen-measure-catalog.mjs — do not edit by hand.
 * The full 60-measure TWH catalog (OSHA safety + HEDIS wellness + CMS eCQM) the
 * /measures page reads, sourced from the Java seed (MeasureService.CMS_ECQM_CATALOG +
 * the OSHA/HEDIS seed + V017). Lifecycle mutations (create/approve/activate/deprecate,
 * spec/CQL edits) are a later slice and need a persisted measures store; this is the
 * read catalog. The 10 Active measures are exactly the engine's runnable set.
 */
export type MeasureStatus = "Draft" | "Approved" | "Active" | "Deprecated";

export interface CatalogMeasure {
  id: string;
  name: string;
  policyRef: string;
  version: string;
  status: MeasureStatus;
  owner: string;
  tags: string[];
}

export const MEASURE_CATALOG: readonly CatalogMeasure[] = [
${body}
];
`;
writeFileSync(outFile, out);
console.log(`wrote ${outFile} (${all.length} measures: ${runnable.length} active runnable, ${catalogOnly.length} catalog-only, ${cms.length} CMS)`);
